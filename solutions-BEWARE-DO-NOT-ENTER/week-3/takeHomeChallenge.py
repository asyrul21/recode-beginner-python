# calculate area of circle
# radius is 5
pi = 3.14159

radius = 5
radiusSquared = radius * radius

area = pi * radiusSquared
print("The area of the circle with radius " + str(radius) + " is " + str(area))


# calculate volume of sphere
# radius is 5
pi = 3.14159

radius = 5
radiusCubic = radius * radius * radius

volume = (4/3) * pi * radiusCubic
print("The volume of the sphere with radius " + str(radius) + " is " + str(volume))